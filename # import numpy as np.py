# import numpy as np
import matplotlib.pyplot as plt

# Create an array of values from 0 to 2*pi with an increment of pi/100
x = np.linspace(0, 2*np.pi, 200)

# Calculate the sine of each value in the array
y = np.sin(x)

# Plot x vs y
plt.figure()
plt.plot(x, y)
plt.xlabel('x')
plt.ylabel('sin(x)')
plt.title('Plot of the Sine Function')
plt.grid(True)
plt.axis('equal')

# Create a new figure with red dashed-line
plt.figure()
plt.plot(x, y, 'r--')
plt.title('Plot of the Sine Function (Red Dashed-line)')
plt.axis('equal')

# Plot Sine and Cosine functions in the same figure
plt.figure()
plt.plot(x, np.sin(x), label='sin')
plt.plot(x, np.cos(x), ':', label='cos')
plt.legend()
plt.title('Plot of Sine and Cosine Functions')
